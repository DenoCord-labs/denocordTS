export enum TimeStamps {
	/**
	 * ShortTime
	 * @example
	 * 16:00
	 */
	"t" = "t",
	/**
	 * Long Time
	 * @example
	 * 16:20:21
	 */
	"T" = "T",
	/**
	 * Short Date
	 * @example
	 * 30/04/2022
	 */
	"d" = "d",
	/**
	 * Long Date
	 * @example
	 * 30 April 2022
	 */
	"D" = "D",
	/**
	 * Short Date/Time
	 * @example
	 * 30 April 2022 16:20
	 */
	"f" = "f",
	/**
	 * Long Date/Time
	 * @example
	 * Saturday, 30 April 2022 16:20
	 */
	"F" = "F",
	/**
	 * Relative Time
	 * @example
	 * 2 days ago
	 */
	"R" = "R",
}
